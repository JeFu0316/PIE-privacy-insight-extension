  var AVAILABLE = Object.keys(MESSAGES);

  function normalize(tag) {
    if (!tag) return null;
    var t = String(tag).replace('_', '-').toLowerCase();
    if (t === 'zh' || t === 'zh-cn' || t === 'zh-hans' || t === 'zh-sg') return 'zh_CN';
    if (t === 'zh-tw' || t === 'zh-hk' || t === 'zh-mo' || t === 'zh-hant') return 'zh_TW';
    if (t.indexOf('pt') === 0) return 'pt_BR';
    var base = t.split('-')[0];
    var map = { en: 'en', ru: 'ru', es: 'es', fr: 'fr', de: 'de', ja: 'ja', ko: 'ko' };
    if (map[base] && AVAILABLE.indexOf(map[base]) !== -1) return map[base];
    return null;
  }

  function detect() {
    var langs = [];
    try {
      if (typeof navigator !== 'undefined') {
        if (navigator.languages && navigator.languages.length) langs = navigator.languages.slice();
        else if (navigator.language) langs = [navigator.language];
      }
    } catch (_) {}
    for (var i = 0; i < langs.length; i++) {
      var n = normalize(langs[i]);
      if (n && AVAILABLE.indexOf(n) !== -1) return n;
    }
    return 'en';
  }

  // 'auto' (or an unavailable value) resolves to the detected browser locale.
  function resolve(pref) {
    if (pref && pref !== 'auto' && AVAILABLE.indexOf(pref) !== -1) return pref;
    return detect();
  }

  var currentLocale = 'en';

  function setLocale(pref) {
    currentLocale = resolve(pref);
    return currentLocale;
  }

  function getLocale() { return currentLocale; }

  function raw(key, locale) {
    var loc = locale || currentLocale;
    var cat = MESSAGES[loc];
    if (cat && cat[key] != null) return cat[key];
    if (MESSAGES.en[key] != null) return MESSAGES.en[key];
    return null;
  }

  function fill(str, subs) {
    if (!subs) return str;
    return str.replace(/\{(\w+)\}/g, function (m, k) {
      return subs[k] != null ? String(subs[k]) : m;
    });
  }

  function t(key, subs) {
    var s = raw(key, currentLocale);
    if (s == null) return key;
    return fill(s, subs);
  }

  // Plural helper: looks up `${key}.one` / `${key}.other` and injects {n}.
  function tn(key, n, subs) {
    var suffix = (n === 1 || n === -1) ? '.one' : '.other';
    var merged = { n: n };
    if (subs) for (var k in subs) if (Object.prototype.hasOwnProperty.call(subs, k)) merged[k] = subs[k];
    var s = raw(key + suffix, currentLocale);
    if (s == null) s = raw(key + '.other', currentLocale);
    if (s == null) return key;
    return fill(s, merged);
  }

  // Translate static DOM: [data-i18n] -> textContent, plus title/aria-label.
  function applyDom(scope) {
    var rootEl = scope || (typeof document !== 'undefined' ? document : null);
    if (!rootEl || !rootEl.querySelectorAll) return;
    rootEl.querySelectorAll('[data-i18n]').forEach(function (el) {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    rootEl.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
    });
    rootEl.querySelectorAll('[data-i18n-label]').forEach(function (el) {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-label')));
    });
    rootEl.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      el.setAttribute('placeholder', t(el.getAttribute('data-i18n-placeholder')));
    });
  }

  root.PIE_I18N = {
    _messages: MESSAGES,
    LOCALE_LABEL: LOCALE_LABEL,
    AVAILABLE: AVAILABLE,
    normalize: normalize,
    detect: detect,
    resolve: resolve,
    setLocale: setLocale,
    getLocale: getLocale,
    t: t,
    tn: tn,
    applyDom: applyDom
  };
